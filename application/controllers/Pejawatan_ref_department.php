class Pejawatan_ref_department extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        $this->load->model('Pejawatan_ref_department_model');
        $this->load->library('form_validation');
    }

    public function index()
    {
        $pejawatan_ref_department = $this->Pejawatan_ref_department_model->get_all();

        $data = array(
            'pejawatan_ref_department_data' => $pejawatan_ref_department
        );

        $this->load->view('pejawatan_ref_department/pejawatan_ref_department_list', $data);
    }

    public function read($id) 
    {
        $row = $this->Pejawatan_ref_department_model->get_by_id($id);
        if ($row) {
            $data = array(
		'id' => $row->id,
		'name' => $row->name,
		'description' => $row->description,
	    );
            $this->load->view('pejawatan_ref_department/pejawatan_ref_department_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('pejawatan_ref_department'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('pejawatan_ref_department/create_action'),
	    'id' => set_value('id'),
	    'name' => set_value('name'),
	    'description' => set_value('description'),
	);
        $this->load->view('pejawatan_ref_department/pejawatan_ref_department_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'name' => $this->input->post('name',TRUE),
		'description' => $this->input->post('description',TRUE),
	    );

            $this->Pejawatan_ref_department_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('pejawatan_ref_department'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Pejawatan_ref_department_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('pejawatan_ref_department/update_action'),
		'id' => set_value('id', $row->id),
		'name' => set_value('name', $row->name),
		'description' => set_value('description', $row->description),
	    );
            $this->load->view('pejawatan_ref_department/pejawatan_ref_department_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('pejawatan_ref_department'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
		'name' => $this->input->post('name',TRUE),
		'description' => $this->input->post('description',TRUE),
	    );

            $this->Pejawatan_ref_department_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('pejawatan_ref_department'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Pejawatan_ref_department_model->get_by_id($id);

        if ($row) {
            $this->Pejawatan_ref_department_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('pejawatan_ref_department'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('pejawatan_ref_department'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('name', 'name', 'trim|required');
	$this->form_validation->set_rules('description', 'description', 'trim|required');

	$this->form_validation->set_rules('id', 'id', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

